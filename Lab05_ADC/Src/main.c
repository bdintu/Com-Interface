void displayHEX(uint16_t dec) {
	char hex[8];
	sprintf(hex, "0x%04X\r\n", dec);
	while(__HAL_UART_GET_FLAG(&huart2, UART_FLAG_TC) == RESET) {}
	HAL_UART_Transmit(&huart2, (uint8_t*)hex, 8, 100);
}

void displayLED(uint16_t dec) {
	static uint16_t step[] = {0x0000, 0x0100, 0x0300, 0x0700, 0x0f00, 0x1f00, 0x3f00, 0x7f00, 0xff00};
	uint16_t level = (dec/456);
	GPIOE->ODR = step[level];
}

